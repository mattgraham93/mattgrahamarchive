/****** Script for SelectTopNRows command from SSMS  ******/
SELECT TOP (1000) [Project_Key]
      ,[Record_Key]
      ,[Project]
      ,[Number]
      ,[DETAILS_Title]
      ,[DETAILS_Description]
      ,[DETAILS_Status]
      ,[DETAILS_Decision_Level]
      ,[DETAILS_Decision_Memo]
      ,[DETAILS_Village_Building_Name]
      ,[DETAILS_Primary_Assignee]
      ,[DETAILS_Date_Opened]
      ,[DETAILS_Decider]
      ,CAST(LEFT([DETAILS_Due_Date],10) as date) as DueDate
      ,[DETAILS_Date_Closed]
      ,[DETAILS_Decision_Outcome]
      ,[DETAILS_Comments]
      ,[SUMMARY_TOTAL]
  FROM [RedmondCR].[dbo].[TEST_msft_issues_detail]